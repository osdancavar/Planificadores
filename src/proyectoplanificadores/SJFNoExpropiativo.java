    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jTextField1.getText()) && !"".equals(jTextField2.getText())) {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            Object[] objTabla = new Object[4];
            objTabla[0] = String.valueOf(proceso);
            objTabla[1] = jTextField1.getText();
            objTabla[2] = jTextField2.getText();
            objTabla[3] = "Preparado";
            modelo.addRow(objTabla);
            jTable1.setModel(modelo);
            jTextField1.setText(null);
            jTextField1.grabFocus();//enfocar
            jTextField2.setText(null);
            jTextField2.grabFocus();//enfocar
            proceso++;
        } else {
            JOptionPane.showMessageDialog(null, "No puede insertar procesos sin rafagas!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


