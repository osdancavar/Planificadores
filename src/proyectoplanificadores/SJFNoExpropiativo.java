    private class Hilo implements Runnable {

        @Override
        public void run() {
           int  tiempo = parseInt((String) (jTable1.getValueAt(0, 2)));
            int tiempo1 = tiempo;
           int  media = tiempo;

            for (int a = 0; a < tiempo; a++) {
                tiempo1--;
                jTable1.setValueAt(tiempo1, 0, 2);
                Dormir();
            }
            jTable1.setValueAt("finalizado ", 0, 3);

//******************************************************************************
            recalcular();
            for (int i = 1; i < proceso - 1; i++) {
                tiempo = parseInt((String) (jTable1.getValueAt(i, 2)));
                tiempo1 = tiempo;
                if (i < proceso - 2) {
                    media = media + (media + tiempo);
                }
                for (int a = 0; a < tiempo; a++) {
                    tiempo1--;
                    jTable1.setValueAt(tiempo1, i, 2);
                    jTable1.setValueAt("ejecutando", i, 3);
                    Dormir();
                }
                jTable1.setValueAt("finalizado", i, 3);
            }
            media = media / proceso;
            jTextField3.setText(String.valueOf(media));
        }
    }

    public void recalcular() {
        for (int i = 0; i < proceso; i++) {
            for (int j = 1; j < proceso - 2; j++) {
                int rafaga = parseInt((String) (jTable1.getValueAt(j, 2)));
                int rafaga1 = parseInt((String) (jTable1.getValueAt(j+1, 2)));
                int tarribo = parseInt((String) (jTable1.getValueAt(j, 1)));
                int tarribo1 = parseInt((String) (jTable1.getValueAt(j+1, 1)));
                if (rafaga > rafaga1) {
                    int proc = parseInt((String) (jTable1.getValueAt(j, 0)));
                    int proc1 = parseInt((String) (jTable1.getValueAt(j+1, 0)));
                    jTable1.setValueAt(String.valueOf(proc1), j, 0);
                    jTable1.setValueAt(String.valueOf(proc), j + 1, 0);
                    jTable1.setValueAt(String.valueOf(tarribo1), j, 1);
                    jTable1.setValueAt(String.valueOf(tarribo), j + 1, 1);
                    jTable1.setValueAt(String.valueOf(rafaga1), j, 2);
                    jTable1.setValueAt(String.valueOf(rafaga), j + 1, 2);
                } else if (rafaga == rafaga1) {
                    if (tarribo > tarribo1) {
                       int proc = parseInt((String) (jTable1.getValueAt(j, 0)));
                    int proc1 = parseInt((String) (jTable1.getValueAt(j+1, 0)));
                        jTable1.setValueAt(String.valueOf(proc1), j, 0);
                        jTable1.setValueAt(String.valueOf(proc), j + 1, 0);
                        jTable1.setValueAt(String.valueOf(tarribo1), j, 1);
                        jTable1.setValueAt(String.valueOf(tarribo), j + 1, 1);
                        jTable1.setValueAt(String.valueOf(rafaga1), j, 2);
                        jTable1.setValueAt(String.valueOf(rafaga), j + 1, 2);
                    }
                }
            }
            
        }
    }

